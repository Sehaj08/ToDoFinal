public class ToDoItems {

    int id;
    String title;
    String category;
    String date;
    String time;

    public ToDoItems(int id, String title, String category, String date, String time) {
        this.id = id;
        this.title = title;
        this.category = category;
        this.date = date;
        this.time = time;
    }
}
